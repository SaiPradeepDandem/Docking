package com.sai.javafx.docking;

/**
 * DockPosition
 */
public enum DockPosition {
  /**
   * Dock to the left using a splitter.
   */
  LEFT,

  /**
   * Dock to the right using a splitter.
   */
  RIGHT,

  /**
   * Dock to the top using a splitter.
   */
  TOP,

  /**
   * Dock to the bottom using a splitter.
   */
  BOTTOM;
}
